package sec02.AlgorithmParctice;

public class AlogoStudent {
    String name ;
    String no;

    public AlogoStudent(String name, String no){
        this.name=name;
        this.no=no;
    }

    public String getName() {
        return name;
    }

    public void setName(String student) {
        this.name = student;
    }

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }
}
