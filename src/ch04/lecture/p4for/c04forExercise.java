package ch04.lecture.p4for;

public class c04forExercise {
    public static void main(String[] args) {

        /*
        0
        2
        4
        6
        8
         */
        for(int i=0; i<=8; i+=2){
            System.out.println(i);
        }

        System.out.println("---------");

        for(int i=2; i<=10; i+=2){
            System.out.println(i);
        }

        System.out.println("---------");

        for(int i=4; i>=0; i--){
            System.out.println(i);
        }

        System.out.println("---------");

        for(int i= 5; i>0; i--){
            System.out.println(i);
        }

    }
}
